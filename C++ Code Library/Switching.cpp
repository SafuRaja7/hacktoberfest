//here i am using Switch to check the valid condition  
#include <iostream> 
using namespace std; 

int main() { 
int x = 5; 
	switch (x) 
	{ 
		case 1: 
			cout << "Choice is 2"; 
			break; 
		case 2: 
			cout << "Choice is 4"; 
			break; 
		case 3: 
			cout << "Choice is 5"; 
			break; 
		default: 
			cout << "Choice other than these cases then"; 
			break; 
	} 
return 0; 
} 